package com.test.ctrlstmts;

public class Switchex {
	public static void main(String[]args){
		int number=3;
		switch(number){
		case 0:
			System.out.println("number is 0");
		break;
		case 1:
			System.out.println("number is 1");
			break;
		case 2:
			System.out.println("number is 2");
			break;
			default:
				System.out.println(number);
			
		}
	}

}
